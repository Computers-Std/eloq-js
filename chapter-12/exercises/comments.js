function skipSpace(string) {
  let ignore = string.match(/^(\s|#.*)*/);
  return string.slice(ignore[0].length);
}

console.log(parse("# hello\nx"));
// → {type: "word", name: "x"}

console.log(parse("a # one\n   # two\n()"));
// → {type: "apply",
//    operator: {type: "word", name: "a"},
//    args: []}
